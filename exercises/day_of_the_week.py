# 3. По заданному номеру дня недели вывести его название

def day_of_the_week(n):
    days = ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday', 'Sunday']
    print(days[n - 1])
    print(days[n-1])


day = int(input('Введите номер дня недели:'))
